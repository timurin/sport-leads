from collections.abc import Mapping


class SportClassifier:
    SPORT_KEYWORDS: Mapping[str, tuple[str, ...]] = {
        "Футбол": (
            "футбол",
            "футбольный",
            "мини-футбол",
            "футзал",
        ),
        "Волейбол": (
            "волейбол",
            "волейбольный",
            "пляжный волейбол",
        ),
        "Баскетбол": (
            "баскетбол",
            "баскетбольный",
            "стритбол",
        ),
        "Хоккей": (
            "хоккей",
            "хоккейный",
            "шайба",
        ),
        "Лёгкая атлетика": (
            "лёгкая атлетика",
            "легкая атлетика",
            "бег",
            "марафон",
            "полумарафон",
            "эстафета",
        ),
        "Плавание": (
            "плавание",
            "пловец",
            "бассейн",
        ),
        "Гандбол": (
            "гандбол",
            "гандбольный",
        ),
        "Регби": (
            "регби",
            "регбийный",
        ),
        "Теннис": (
            "теннис",
            "теннисный",
        ),
        "Настольный теннис": (
            "настольный теннис",
            "пинг-понг",
        ),
        "Лыжный спорт": (
            "лыжные гонки",
            "лыжный спорт",
            "лыжник",
        ),
        "Фигурное катание": (
            "фигурное катание",
            "фигурист",
        ),
        "Гимнастика": (
            "гимнастика",
            "гимнастический",
        ),
        "Единоборства": (
            "единоборства",
            "дзюдо",
            "самбо",
            "карате",
            "тхэквондо",
            "бокс",
        ),
    }

    def classify(self, text: str) -> str | None:
        normalized_text = text.casefold()

        scores: dict[str, int] = {}

        for sport, keywords in self.SPORT_KEYWORDS.items():
            score = sum(
                normalized_text.count(keyword.casefold())
                for keyword in keywords
            )

            if score > 0:
                scores[sport] = score

        if not scores:
            return None

        return max(scores, key=scores.get)